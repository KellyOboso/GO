#arithmetic operators
num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #modulus -returns remainder after dividing the operants
print(4 % 2)

#comparison operators - used to compare values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #equal to
print(num1 != num2)#not equal to

#assignment operators
number = 12
print(number)

number += 2
print(number)

b = 56
b -= 2
print(b)

#Logic Operators - and or not
print( 34 < 10 and 89 > 5 ) #true if all are true
print( 34 < 10 or 89 > 5 )#true if any of the conditions is true
print(not (34 < 10 or 89 > 5 ))#reverses the final result








